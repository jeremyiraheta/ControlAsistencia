/*==============================================================*/
/* Table: CLIENTES                                             */
/*==============================================================*/
create table CLIENTES
(
   CODCLI               int not null auto_increment,
   NOMBRE               varchar(200) not null,
   CORREO_CONTACTO      varchar(200) not null,
   TELEFONO_CONTACTO    varchar(100) not null,
   FECHA_REGISTRO       date not null,
   FECHA_FIN_SERVICIO   date not null,
   PLAN                 int not null,
   CAPTURARPANTALLA     enum('true','false'),
   CAPTURARPROCESOS     enum('true','false'),
   CAPTURARHISTORIALNAV enum('true','false'),
   ZONAHORARIA          int not null,
   PAIS                 varchar(50) not null,
   INVERVALO            INT DEFAULT 60,
   PORCTCAPT            FLOAT DEFAULT 0.5,
   ACTIVO               enum('true','false'),
   primary key(CODCLI)
);
/*==============================================================*/
/* Table: PRODUCTIVIDAD                                             */
/*==============================================================*/
create table PRODUCTIVIDAD
(
   CODPROD              int not null auto_increment,
   CODCLI               int not null,
   PROCESOS             mediumtext,
   HISTNAV              mediumtext,
   FECHA                time not null,
   primary key(CODPROD, CODCLI),
   foreign key(CODCLI) references CLIENTES(CODCLI)
);

/*==============================================================*/
/* Table: DEPARTAMENTOS                                         */
/*==============================================================*/
create table DEPARTAMENTOS
(
	CODDPTO					INT NOT NULL AUTO_INCREMENT,
   CODCLI               int not null,
   NOMBRE               varchar(200) not null,
   primary key (CODDPTO,CODCLI),
   foreign key (CODCLI) references CLIENTES(CODCLI)
);

/*==============================================================*/
/* Table: EMPLEADOS                                             */
/*==============================================================*/
create table EMPLEADOS
(
   CODEMP               int not null auto_increment,
   CODCLI               int not null,
   CODDPTO              INT NOT NULL,
   NOMBRES              varchar(200) not null,
   APELLIDOS            varchar(200) not null,
   TELEFONOS            varchar(20),
   CORREO               varchar(50),
   GENERO               char(1) not null,
   NACIMIENTO           date not null,
   DIRECCION            varchar(1000) not null,
   DUI                  varchar(10) not null,
   NIT                  varchar(20),
   AFP                  varchar(12),
   USUARIO              varchar(50) not null unique,
   PASSWORD             varchar(64) not null,
   ESTADO               char(1) not null,
   primary key (CODEMP, CODCLI),
   foreign key(CODCLI) references CLIENTES(CODCLI)
);

/*==============================================================*/
/* Table: PERMISOS                                              */
/*==============================================================*/
create table PERMISOS
(
   CODPER               int not null auto_increment,
   CODEMP               int not null,
   CODCLI               int not null,
   FECHA                date not null,
   TIPO                 char(1) not null,
   DESCRIPCION          VARCHAR(1000) not null,
   HORAINICIAL          time not null,
   HORAFINAL            time not null,
   ESTADO               char(1) not null,
   primary key (CODPER),
   foreign key(CODEMP, CODCLI) references EMPLEADOS(CODEMP, CODCLI)
);

/*==============================================================*/
/* Table: REGISTROS                                             */
/*==============================================================*/
create table REGISTROS
(
   CODEMP               int not null,
   CODCLI               int not null,
   FECHA                date not null,
   HORAENTRADA          time not null,
   HORASALIDA           time,
   primary key (CODEMP, CODCLI,FECHA),
   foreign key(CODEMP, CODCLI) references EMPLEADOS(CODEMP, CODCLI)
);

